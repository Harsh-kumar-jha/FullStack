package com.chapter3.controlFlow;



public class ProgramOnIfElseIfStatement {
	public static void main(String[] args) {
		int x = 2;
		
		if(x > 10) {
			System.out.println("x value is greater than 10");
		}else if(x < 10) {
			System.out.println("x value is less than 10");
		}else {
			System.out.println("x value is 10");
		}
	}
}	
