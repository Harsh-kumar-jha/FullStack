package com.chapter2.operators;

public class RelationalOperators {
	public static void main(String[] args) {
		int a = 40;
		int b = 30;
		
		System.out.println("a == b : "+ (a == b));
		System.out.println("a != b : "+ (a != b));
		System.out.println("a > b : "+ (a > b));
		System.out.println("a >= b : "+ (a >= b));
		System.out.println("a < b : "+ (a < b));
		System.out.println("a <= b : "+ (a <= b));
	}
}
